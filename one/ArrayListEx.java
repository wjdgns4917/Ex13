package com.one;

import java.util.ArrayList;
import java.util.Scanner;

public class ArrayListEx {
	public static void main(String[] args) {
		//문자열만 삽입 가능한 ArryList생성
		ArrayList<String>a=new ArrayList<String>();
		
		//키보드로부터 4개의 이름 입력받아 ArrayList에 삽입
		Scanner sc=new Scanner(System.in);//Scanner 객체 생성
		for (int i = 0; i < 4; i++) {
			System.out.println("이름을 입력하세요>>");
			String s=sc.next();//키보드로부터 이름을 입력
			a.add(s);//ArrayList컬렉션에 삽입
		}
		//ArrayList에 들어있는 모든이름 출력
		for (int i = 0; i < a.size(); i++) {
			String name=a.get(i);//ArrayList의 i번째 문자열 얻어오기
			System.out.print(name+" ");
			System.out.println(a.get(i).length());
		}
		//가장 긴이름 출력
		int longstIndex=0;//현재 가장 긴이름이 있는 ArrayList내의 인덱스
		for (int i = 1; i < a.size(); i++) {
			if(a.get(longstIndex).length()<a.get(i).length())//이름 길이 비교
				longstIndex=i;//i번째 이름이 더 긴이름임
		}
		System.out.println("\n가장 긴 이름은 : "+a.get(longstIndex));
		sc.close();
	}
}
